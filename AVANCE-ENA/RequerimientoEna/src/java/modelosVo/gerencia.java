
package modelosVo;

public class gerencia {
    private int num;
    private String nombre;

    public gerencia() {
    }

    public gerencia(int num, String nombre) {
        this.num = num;
        this.nombre = nombre;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
